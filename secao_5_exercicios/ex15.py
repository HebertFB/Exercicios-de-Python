dia = int(input('Informe um número de 1 à 7 para verificar o dia da semana: '))

if dia == 1:
    print('\nHoje é Domingo')
elif dia == 2:
    print('\nHoje é Segunda-Feira')
elif dia == 3:
    print('\nHoje é Terça-Feira')
elif dia == 4:
    print('\nHoje é Quarta-Feira')
elif dia == 5:
    print('\nHoje é Quinta-Feira')
elif dia == 6:
    print('\nHoje é Sexta-Feira')
elif dia == 7:
    print('\nHoje é Sábado')
else:
    print('\nNúmero Inválido!!!')

